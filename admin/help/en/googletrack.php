<h3>Analytics tracking code</h3>
<p>If you use Google Analytics or Matomo on your website this is a great way to see the results of your campaigns.</p>
<p>Selecting this option will add some tracking code to all links. The parameters will depend on whether Google Analytics
or Matomo has been selected in the Campaigns section on the Settings page.
phpList provides default values for the parameters, which you can change.</p>
<p>After sending your campaign, check your Google Analytics or Matomo account dashboard to see the results.</p>
<p>Do not use this if your campaign already contains analytics tracking links, because existing ones will be removed.</p>
